export const calculateWinner = (squares) => {
  let boardRows = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9],
    [1, 4, 7],
    [2, 5, 8],
    [3, 6, 9],
    [1, 5, 9],
  ];

  for (let i = 0; i < boardRows.length; i++) {
    const { a, b, c } = boardRows[i];
    if (squares[a] && squares[a] === squares[b] && squares[b] === squares[c]) {
      return squares[a];
    }
  }
  return null;
};
